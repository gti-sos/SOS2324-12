  // Función para calcular la media de los precios
  // en una ciudad específica

  function mean_price_by_city(data,searchCity){
    var airbnb_in_city = data.filter(airbnb => airbnb.city === searchCity);

    if (airbnb_in_city.length === 0){
        console.log("No se encontraron alojamientos en ${searchCity}");
        return 0;
    }

    var prices = airbnb_in_city.map(airbnb => airbnb.price)
    var prices_total_sum = prices.reduce((total,price) => total + price,0);
    var prices_mean = prices_total_sum / prices.length;

    return prices_mean;
  }

